package DB;

public class Debts {
    private String TC;
    private float debts;
    private int bookID;

    public String getTC() {
        return TC;
    }

    public void setTC(String TC) {
        this.TC = TC;
    }

    public float getDebts() {
        return debts;
    }

    public void setDebts(float debts) {
        this.debts = debts;
    }

    public int getBookID() {
        return bookID;
    }

    public void setBookID(int bookID) {
        this.bookID = bookID;
    }
}
